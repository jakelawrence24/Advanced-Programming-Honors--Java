/*
 * This class demonstrates Java arithmetic operators
 */

public class LawrenceOperators {
    
    //calculates and prints values for the Java arithmetic operators
    public static void main(String[] args) {
        int n1 = 4;
        int n2 = 5;
        int n3 = 6;
        System.out.println("n1 = " + n1);
        System.out.println("n2 = " + n2);
        System.out.println("n3 = " + n3);
        System.out.println("n1 + n2 = " + (n1 + n2));
        System.out.println("n3 + n1 = " + (n1 + n3));
        System.out.println("n3 * n1 = " + (n1 * n3));
        System.out.println("n3 / n1 = " + (n3 / n1));
        System.out.println("n3 % n1 = " + (n3 % n1));
        System.out.println("(n1 + n2) * n3 = " + (n1 + n2) * n3);
    }}